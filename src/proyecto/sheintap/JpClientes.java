    private void labelEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelEliminarMouseClicked
        // TODO add your handling code here:
        // TODO add your handling code here:
        //Obtener la fila seleccionada. 
        int fila = TablaDatos.getSelectedRow();
        if(fila!=-1)
        {
            String id=(String) modelo.getValueAt(fila,0);
            //La ventana de confirmación retorna un valor numerico equivalente a la opcion que selecciona el usuario. 
            int op=JOptionPane.showConfirmDialog(null, "Está a punto de eliminar permanentemente al empleado '"+id+"'", "Confirmar eliminacion", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            //Si confirma la eliminación, el valor de op=0
            if(op==0){
                seleccion= TablaDatos.rowAtPoint(evt.getPoint());
                 numcel = String.valueOf(TablaDatos.getValueAt(seleccion, 1));
                
                a.eliminarCliente(numcel);
                verTodos();
            }else{
                //Sí cancela la eliminacion no pasa nada
            }
        }else{
            JOptionPane.showMessageDialog(null,"Selecciona a un empleado y presiona 'Eliminar'" ,"Busqueda de empleados",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_labelEliminarMouseClicked

