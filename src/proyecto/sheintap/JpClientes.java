    //Metodo que genera una nueva tabla con nuevos datos.(Actualiza la vista de la tabla)
    //Se tienen que crear un nuevo modelo en cada consulta para que los registros se "limpien" en cada consulta
    //Si se usa un solo modelo, los resultados de las nuevas consultas se insertan debajo de los viejos resultados.
    private void generarTabla(){
        //Se crea un nuevo modelo de tabla
        modelo = new DefaultTableModel();
        //Se agregan las columnas
        modelo.setColumnIdentifiers(new Object[]{"Nombre","Celular","Ciudad"});
        //Se agregan las filas. 
        for(int x=0;x<clientes[0].length;x++){
            modelo.addRow(new Object[]{""+clientes[1][x],""+clientes[0][x],""+clientes[2][x]});
        }
        //Se añade el nuevo modelo a la tabla
        TablaDatos.setModel(modelo);
        
 
    }

